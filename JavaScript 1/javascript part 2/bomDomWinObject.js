// BOM- browser object model
// Bomb are some additional objects provided by the host basically browser. For example alert confirm prompt ETC.
location.href="www.Google.com" // this is also a bom.

// DOM- document object model
// Our entire JavaScript model combined creates an object which is called document.
document.body.style.background="red" // this can be used to change CSS realtime as well. mostly done for user input.